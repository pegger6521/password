# 密碼重試程式
# 密碼為"a123456"
# 讓使用者最多輸入3次
# 不對的話, 就印出"密碼錯誤! 還有__次機會!"
# 對的話, 就印出"登入成功!"

password = 'a123456'
i = 3 # 剩餘機會
while i > 0: 
	pwd = input('請輸入密碼: ')
	i -= 1 # 這是 i = i - 1的簡寫版本
	if pwd == 'a123456':
		print('登入成功!')
		break
	else:
		print('密碼錯誤!')
		if i > 0:
			print('還有', i, '次機會')
		else:
			print('你帳號被鎖起來啦')


